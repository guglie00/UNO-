	<button class="pannello">Le Funzioni delle carte azione</button>
	<div class="contenuto">Le funzioni delle carte "Azione" sono le seguenti:

   <br> Carta Inverti o Cambio giro: inverte il senso di gioco (l'ordine dei turni dei giocatori) da orario ad antiorario o viceversa.
    <br>Carta Salto o Salta turno o Stop: fa saltare un turno al giocatore successivo nel senso del gioco.
    <br>Carta Pesca due carte: il giocatore successivo nel senso del gioco pesca due carte e salta il turno.
    <br>Carta Jolly o Cambio colore: permette al giocatore di scegliere uno tra i quattro colori disponibili (Rosso, Verde, Blu, Giallo), che il giocatore deve dichiarare giocando la carta. Può essere giocata in qualsiasi momento e su qualsiasi carta, indipendentemente dal fatto che si abbia o meno una carta giocabile.
   <br> Carta Jolly Pesca quattro o Pesca quattro carte: è la carta più potente del gioco. Costringe il giocatore successivo nel senso del gioco a pescare quattro carte ed a saltare il turno. La carta in questione consente, inoltre, a chi l'ha giocata di cambiare il colore del gioco. A differenza della carta "Jolly", può essere giocata solo se non si hanno altre possibilità di gioco: nel caso si pensi che sia stata giocata illegalmente, si applicano le penalità descritte nella sezione "Penalità"</div>
	<button class="pannello">Obiettivo del gioco</button>
	<div class="contenuto">Il giocatore che per primo riesce a esaurire tutte le carte che ha nella sua mano vince la partita. Nella versione inglese, invece, ogni giocatore che esaurisce le carte che ha nella mano ottiene punti in base alle carte rimaste in mano agli altri giocatori; il primo giocatore che raggiunge i 500 punti vince la partita</div>
	<button class="pannello">Procedimento del gioco</button>
	<div class="contenuto">Il sorteggio del mazziere avviene mediante pesca di una carta dal mazzo; colui che pesca quella con il numero più alto diviene il mazziere (in questa fase del gioco le carte Azione hanno valore pari a zero). Il mazziere mescola il mazzo e poi distribuisce 7 carte a ciascun giocatore. La porzione di mazzo rimanente, detta "mazzo pesca", viene posata sul tavolo a dorso coperto, e la prima viene girata, andando così a formare il cosiddetto "mazzo scarti". Nel caso in cui la prima carta sia una carta Azione, è necessario applicare le regole della sezione Funzioni delle carte Azione; fa eccezione la carta "Jolly Pesca Quattro" che, se scoperta all'inizio del gioco, deve essere rimessa a caso nel "mazzo pesca" e sostituita con un'altra.

Il giocatore alla sinistra del mazziere comincia il gioco (procedendo il giro in senso orario): questi deve scartare una carta dalle proprie a disposizione, compatibile con quella in cima al mazzo scarti per colore o per numero. Ciò significa che la carta deve avere colore o numero uguale alla prima carta sul banco: ad esempio, se la carta sul banco è un 7 rosso, il giocatore può scartare una qualsiasi carta numerata di colore rosso oppure un 7 di qualsiasi colore; da allora il colore può cambiare e così via. Alternativamente, il giocatore può usare una carta Azione (le carte azione colorate devono essere compatibili con la carta del mazzo scarti). Non è possibile scartare più di una carta per turno.

Se il giocatore non ha a disposizione carte da scartare, ha l'obbligo di pescarne una dal mazzo pesca; se la carta pescata può essere giocata questi lo può fare immediatamente, altrimenti dichiara di passare il turno al giocatore successivo. Il giocatore ha l'obbligo di tirare una carta in suo possesso, se compatibile con quella che si trova sul mazzo scarti. In alternativa il giocatore può scegliere di non giocare una carta giocabile dalla sua mano e pescare una carta dal mazzo. Se può e vuole giocare la carta che pesca, la può utilizzare nello stesso turno, ma non può scegliere una carta di quelle della sua mano dopo che ha fatto una pescata.

Quando un giocatore scarta una delle sue due carte rimaste così rimanendo con una sola carta in mano, deve pronunciare "UNO!". La pronuncia della parola deve avvenire prima che la penultima carta che questi gioca raggiunga il mazzo scarti e deve essere detta comprensibilmente.

Quando un giocatore resta senza carte in mano vince la mano e si procede al calcolo del punteggio conseguito. Nel caso in cui l'ultima carta da giocare sia un Pesca due carte o un Jolly Pesca quattro, il giocatore seguente deve prendere il numero di carte corrispondenti (che saranno tenute in conto per il calcolo del suo punteggio). </div>
	<button class="pannello">Penalità</button>
	<div class="contenuto">Se ci si dimentica di esclamare "UNO!" quando si scarta la penultima carta e la cosa viene notificata da un altro giocatore, si devono pescare 2 carte di penalità dal mazzo pesca. Non si incappa nella penalità se non si esclama "UNO!" ed il giocatore successivo consuma il suo turno senza che nel frattempo nessuno se ne accorga.
    <br>I giocatori che suggeriscono agli altri che carta giocare devono pescare 4 carte dal mazzo.
    <br>Se si sospetta che la carta Jolly Pesca Quattro sia stata giocata bluffando, cioè quando il giocatore che la gioca aveva anche altre carte giocabili in quel momento, il giocatore cui spetta pescare le quattro carte può muovere verso il giocatore della carta un'accusa in tal senso, e quest'ultimo ha l'obbligo di mostrargli le proprie carte. Se la carta è stata giocata correttamente, colui che ha mosso l'accusa deve pescare 6 carte dal mazzo pesca (4 del Jolly + 2 di penalità) , altrimenti se la carta è stata giocata illegalmente è chi l'ha giocata a dover pescare quattro carte. L'accusa può essere mossa solo dal giocatore cui spetterebbe prelevare le quattro carte.
   <br>Il giocatore che alla fine ha più carte in mano subisce una penalità maggiore degli altri.</div>
	
	
